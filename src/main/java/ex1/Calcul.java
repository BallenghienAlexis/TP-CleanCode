package ex1;

/**
 * Classe de calcul avec des méthodes d'addition et de soustraction.
 */
public class Calcul {

    /**
     * Additionne deux entiers.
     *
     * @param nb1 le premier entier
     * @param nb2 le deuxième entier
     * @return la somme de nb1 et nb2
     */
    public int addition(int nb1, int nb2) {
        return nb1 + nb2;
    }

    /**
     * Soustrait le deuxième entier du premier.
     *
     * @param nb1 le premier entier
     * @param nb2 le deuxième entier
     * @return la différence entre nb1 et nb2
     */
    public int soustraction(int nb1, int nb2) {
        return nb1 - nb2;
    }
}
